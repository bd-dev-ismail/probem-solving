//Even Finder Function
function isEven(number){
    if(number % 2===0){
        return true;
    }
    else{
        return false;
    }
}
console.log(isEven(98));
//Odd Finder Function
function isOdd(num){
    if(num % 2 ===1){
        return true;
    }
    else{
        return false;
    }
}
console.log(isOdd(175));